// Program 17: Check if a number is within the range 10 to 20 (inclusive)

let testNumber = prompt('enter the number:');

if(testNumber>=10 && testNumber<=20)
{
    document.write(testNumber + " is within the range 10 to 20.");

}
else
{
  document.write(testNumber + " is outside the range 10 to 20.");

}